package pl.Karmelita;

public class Number {
    private int value;
    private int iloscWyst;

    public Number(int value, int iloscWyst) {
        this.value = value;
        this.iloscWyst = iloscWyst;
    }

    public int getValue() {
        return value;
    }

    public int getIloscWyst() {
        return iloscWyst;

    }

    public void setIloscWyst(int iloscWyst) {
        this.iloscWyst = iloscWyst;
    }
}




